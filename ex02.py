num1 = 10
num2 = 3


#연산자
#+:덧셈, -:뺄셈, *:곱셈, /:나눗셈, **:제곱근, %:나머지, //:몫(정수)
print(num1 * num2)
print(num1 / num2)
print(num1 // num2)
print(num1 + num2)
print(num1 - num2)

#문자열 곱하기 *
print("=" * 20)

#문자열 더하기 +
head = "Python"
tail = "is funs"
print(head+tail)
print(head*2)
#문자열 길이구하기 1en(변수)
a = "python"
print(len(a))

#문자열 인덱싱
print(a[5])
#문자열 슬라이싱 문자열[시작번호: 끝번호] - 끝번호 제외
print(a[0:2])
print(a[:3])
print(a[2:])